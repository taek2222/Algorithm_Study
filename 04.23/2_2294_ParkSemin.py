# 1. ì…ë ¥ ë°›ê¸°
n, k = map(int, input().split())
coins = [int(input()) for _ in range(n)]

# 2. dp
dp = [ [0] + [10001]*k ] * (n+1)
for i in range(1, n+1):
    dp[i] = dp[i-1].copy()
    coin = coins[i-1]
    for j in range(coin, k+1):
        dp[i][j] = min(dp[i-1][j], dp[i][j-coin] + 1)

# 3. ì¶œë ¥
print(dp[n][k]) if dp[n][k] != 10001 else print(-1)